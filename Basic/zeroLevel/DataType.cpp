#include<iostream>
using namespace std;
int main() {
  
//Integer data type 
int apples = 5;
cout<<apples<<endl;
cout<<"Size of apple is : "<<sizeof(apples)<<endl;


//char Datatype 
char sign = '&';
cout<<sign<<endl;
cout<<"size of char is : "<<sizeof(sign)<<endl;

//boolean Data type 
bool flag = true;
cout<<flag<<endl;
cout<<"Size of boolen is : " <<sizeof(flag)<<endl;


// float Data type 
float score = 3.14;
cout<<score<<endl;
cout<<"Size of score is : "<<sizeof(score)<<endl;



return 0 ;
}